    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $trip= new Trip();
        $trip->date_pickup=substr($request->dateRequired,0,10);
        $trip->date_done=substr($request->dateDone,0,10);
        $trip->id_vehicle=$request->idVehicle;
        $trip->comments=$request->comments;
        $trip->particulars=$request->particulars;
        $trip->driver=$request->driver;
        $trip->save();
    }

